package Udemic;

public class U4_112 {
    public static void main(String[] args) {
        System.out.println(28 + 5 <= 4 + 29);  // true
        System.out.println(33 <= 33);          // true
 
        System.out.println((28 + 5) <= (4 + 29));  // true
        System.out.println((33) <= (33));          // true
        System.out.println(33 <= 33);              // true
    }
}