package Udemic;

import java.util.Arrays;

public class U2_Q2068 {
    public static void main(String[] args) {
        boolean[][] bools[], moreBools;
        bools = new boolean[1][1][1];
        moreBools = new boolean[1][1];
        System.out.println(Arrays.deepToString(bools));
        // [[[false]]]
        System.out.println(Arrays.deepToString(moreBools));
        // [[false]]
    }
}
