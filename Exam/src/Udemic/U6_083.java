package Udemic;

public class U6_083 {
    public static void main(String[] args) {
        System.out.println(2 + 3 + 4 + "!");    // 9!
        System.out.println((2 + 3) + 4 + "!");  // 9!
        System.out.println(5 + 4 + "!");        // 9!
        System.out.println((5 + 4) + "!");      // 9!
        System.out.println(9 + "!");            // 9!
        System.out.println("9!");               // 9!
 
        System.out.println(2 + 3 * 4 + "!");    // 14!
        System.out.println(2 + (3 * 4) + "!");  // 14!
        System.out.println(2 + 12 + "!");       // 14!
        System.out.println((2 + 12) + "!");     // 14!
        System.out.println(14 + "!");           // 14!
        System.out.println("14!");              // 14!
    }
}
