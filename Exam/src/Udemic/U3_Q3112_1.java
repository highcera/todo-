package Udemic;

public class U3_Q3112_1 {
    public void run() {
        System.out.print("No args! ");
    };
    protected void run(int arg) {
        System.out.print(arg + " ");
    };
    void run(double arg) {
        System.out.print(arg + " ");
    };
    private void run(String arg) {
        System.out.print("\"" + arg + "\"");
    };
}
